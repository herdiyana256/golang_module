/*Operator Logika dan Percabangan:

Operator logika sering digunakan dalam percabangan (if-else) untuk menentukan alur program berdasarkan kondisi tertentu. Contohnya

*/


package main 

import "fmt"

func main() {
	age := 18 

	if age >= 18 {
		fmt.Println("anda sudah dewasa")
	} else {
		fmt.Println("anda belum dewasa")
	} // anda sudah dewasa
}